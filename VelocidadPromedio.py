# Calcula la velocidad promedio de un viaje.

tiempo = int(input("Teclea el tiempo del viaje en horas enteras: "))
distancia = int(input("Teclea la distancia del viaje en kilómetros enteros: "))

velocidad = distancia / tiempo

print("La velocidad promedio es: ", velocidad, "km/h")

